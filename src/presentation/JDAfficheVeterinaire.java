private void Filtre() {
        String nom = tfNom.getText();
        String prenom = tfPrenom.getText();
        String ville = tfVille.getText();
        nom = nom + "%";
        prenom = prenom + "%";
        ville =ville + "%";
        myTableVeterinaire.setMyList(daoVeterinaire.selectPersonneParFiltre(nom, prenom, ville));
    }
