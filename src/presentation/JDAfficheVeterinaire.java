    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed

        if (jTable1.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Séléctionnez d'abord une ligne svp !", "Avertissement", JOptionPane.ERROR_MESSAGE);
        } else {
            myVeterinaire = myTableVeterinaire.getMyList(jTable1.getSelectedRow());
            JDUpdateVeterinaire jd = new JDUpdateVeterinaire(null, rootPaneCheckingEnabled, myVeterinaire);
            jd.setVisible(true);
            refreshModel();
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (jTable1.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Séléctionnez d'abord une ligne svp !", "Avertissement", JOptionPane.ERROR_MESSAGE);
        } else {
            myVeterinaire = myTableVeterinaire.getMyList(jTable1.getSelectedRow());
            int rep = JOptionPane.showConfirmDialog(null, "Etes-vous sûr de vouloir supprimer l'élément en cascade ?"
                    + myTableVeterinaire.getValueAt(jTable1.getSelectedRow(), 0) + " ?", "Confirmation", JOptionPane.YES_NO_OPTION);
            if (rep == JOptionPane.YES_OPTION) {
                daoVeterinaire.deleteVeterinaire(myVeterinaire);
            }

            refreshModel();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if (jTable1.getSelectedRow() != -1) {
            if (evt.getClickCount() == 2) {
                myVeterinaire = myTableVeterinaire.getMyList(jTable1.getSelectedRow());
                if (myVeterinaire != null) {
                    this.dispose();
                }
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

