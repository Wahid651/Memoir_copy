    private void btnModifActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifActionPerformed
        if (jTable1.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Séléctionnez d'abord une ligne svp !", "Avertissement", JOptionPane.ERROR_MESSAGE);
        } else {

            Medicament med = myTableMedic.getMyList(jTable1.getSelectedRow());

            JDModifMedicament jd = new JDModifMedicament(null, true, med);
            jd.setVisible(true);

            refreshModel();
        }
    }//GEN-LAST:event_btnModifActionPerformed

    private void btnSupprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSupprimerActionPerformed

        if (jTable1.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Séléctionnez d'abord une ligne svp !", "Avertissement", JOptionPane.ERROR_MESSAGE);
        } else {
            Medicament medc = myTableMedic.getMyList(jTable1.getSelectedRow());

            int rep = JOptionPane.showConfirmDialog(null, "Voulez-vous vraiment supprimer "
                    + myTableMedic.getValueAt(jTable1.getSelectedRow(), 0) + " ?", "Confirmation", JOptionPane.YES_NO_OPTION);

            if (rep == JOptionPane.YES_OPTION) {

                daomed.deleteMedicament(medc);
            }

            refreshModel();
        }
    }//GEN-LAST:event_btnSupprimerActionPerformed

